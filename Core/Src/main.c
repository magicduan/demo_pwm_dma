void color_convert(uint8_t* buffer,uint32_t led_color)
{ 
    uint8_t bit_value = 0;
    for(int i = 0; i < 24; i++){
      bit_value = (led_color >> (23 - i)) & 0x01; 
      if (bit_value == 0){
        buffer[i] = DUTY_0;
      }else {          
        buffer[i] = DUTY_1;
      }

    }
}

void make_led_pwm(uint32_t led_color)
{
  int i = 0;
  uint8_t* p_buffer;
    // TRST Buffer
  while (i < TRST_LEN){
    pwm_buffer[i] = DUTY_RESET;
    i++;
  }

  p_buffer = pwm_buffer + i;
  while (i < BUFFER_LED_LEN + TRST_LEN){
      color_convert(p_buffer,led_color);
      i += 24;
      p_buffer += 24;
  }

  while(i < BUFFER_LEN){
    pwm_buffer[i] = DUTY_RESET;
    i++;
  }
}

